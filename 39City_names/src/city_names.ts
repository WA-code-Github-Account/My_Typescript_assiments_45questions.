// Function for city_country
function city_country(city: string, country: string): string {
    return `${city}, ${country}`;
  }
  
  // Result
  console.log(city_country("\nKarachi", "Pakistan."));
  console.log(city_country("\nchicago", "USA."));
  console.log(city_country("\nParis", "France.\n"));